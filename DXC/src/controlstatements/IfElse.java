package controlstatements;

public class IfElse {
	public static void main(String[] args) {
		int number = 25;
		//check if the number is divided by 2 or not
		if(number % 2 == 0) {
			System.out.println("even number");
			
		}else {
			System.out.println("odd number");
			
		}
	}

}
